public class MenuCalculadora {
    public float multi(float But1, float But2) {
		return But1 * But2;
	}
	public float divi(float But1, float But2) {
		return But1 / But2;
	}
	public float subi(float But1, float But2) {
		return But1 - But2;
	}
	public float soma(float But1, float But2) {
		return But1 + But2;
                
	}
    }
